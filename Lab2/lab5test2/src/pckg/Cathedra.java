package pckg;

public class Cathedra {
    private Integer id;
    private String name;
    private String phone;
    private Integer professorRate;
    private Integer docentRate;
    private Integer seniorTeacherRate;
    private Integer teacherRate;
    private Integer professorHours;
    private Integer docentHours;
    private Integer seniorTeacherHours;
    private Integer teacherHours;

    public Cathedra() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public Integer getProfessorRate() {
        return professorRate;
    }

    public void setProfessorRate(Integer professorRate) {
        this.professorRate = professorRate;
    }

    public Integer getDocentRate() {
        return docentRate;
    }

    public void setDocentRate(Integer docentRate) {
        this.docentRate = docentRate;
    }

    public Integer getSeniorTeacherRate() {
        return seniorTeacherRate;
    }

    public void setSeniorTeacherRate(Integer seniorTeacherRate) {
        this.seniorTeacherRate = seniorTeacherRate;
    }

    public Integer getTeacherRate() {
        return teacherRate;
    }

    public void setTeacherRate(Integer teacherRate) {
        this.teacherRate = teacherRate;
    }

    public Integer getProfessorHours() {
        return professorHours;
    }

    public void setProfessorHours(Integer professorHours) {
        this.professorHours = professorHours;
    }

    public Integer getDocentHours() {
        return docentHours;
    }

    public void setDocentHours(Integer docentHours) {
        this.docentHours = docentHours;
    }

    public Integer getSeniorTeacherHours() {
        return seniorTeacherHours;
    }

    public void setSeniorTeacherHours(Integer seniorTeacherHours) {
        this.seniorTeacherHours = seniorTeacherHours;
    }

    public Integer getTeacherHours() {
        return teacherHours;
    }

    public void setTeacherHours(Integer teacherHours) {
        this.teacherHours = teacherHours;
    }
}
